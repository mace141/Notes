def max_path_sum(grid):
  return _max_path_sum(grid, 0, 0, {})
  
def _max_path_sum(grid, r, c, memo):
  if (r, c) in memo:
    return memo[(r, c)]
  if r == len(grid) or c == len(grid[0]):
    return 0
  sum = grid[r][c]
  sum += max(
    _max_path_sum(grid, r + 1, c, memo),
    _max_path_sum(grid, r, c + 1, memo)
    )

  memo[(r, c)] = sum
  return sum

grid = [
  [1, 3, 12],
  [5, 1, 1],
  [3, 6, 1],
]
print(max_path_sum(grid)) # -> 18
